    private void jTableHoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableHoaDonMouseClicked
        // TODO add your handling code here:
        int row = jTableHoaDon.getSelectedRow();
        HoaDonViewModel hd = hoaDonService.getAll().get(row);
        String ma = hd.getMa();
        txtId.setText(hoaDonService.getKHNV(ma).getiD());
        cbbTenKhachHang.setSelectedItem(hoaDonService.getKHNV(ma).getKhachHang().getiD());
        cbbTenNhanVien.setSelectedItem(hoaDonService.getKHNV(ma).getNhanVien().getiD());
        txtMa.setText(ma);
        txtDiaChi.setText(hd.getDiaChi());
        txtNgayNhan.setText(String.valueOf(hd.getNgayNhan()));
        txtNgayShip.setText(String.valueOf(hd.getNgayShip()));
        txtNgayTao.setText(String.valueOf(hd.getNgayTao()));
        txtThanhToan.setText(String.valueOf(hoaDonService.getKHNV(ma).getNgayThanhToan()));
        txtSDT.setText(hd.getSdt());
        if(hd.getTrangThai() == 0){
            rbDaThanhToan.setSelected(true);
        }else{
            rbChuaThanhToan.setSelected(true);
        }
        txtNguoiNhan.setText(hd.getTenNguoiNhan());
    }//GEN-LAST:event_jTableHoaDonMouseClicked

