    private void btnThemHoaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemHoaDonActionPerformed
        // TODO add your handling code here:
        String tenKhachHang = cbbTenKhachHang.getSelectedItem().toString();
        String tenNhanVien = cbbTenNhanVien.getSelectedItem().toString();
        String ma = txtMa.getText();
        String ngayTao = txtNgayTao.getText();
        String ngayThanhToan = txtThanhToan.getText();
        String ngayShip = txtNgayShip.getText();
        String ngayNhan = txtNgayNhan.getText();
        int tinhTrang;
        if(rbDaThanhToan.isSelected()){
            tinhTrang =0;
        }else{
            tinhTrang =1;
        }
        String tenNguoiNhan = txtNguoiNhan.getText();
        String diaChi = txtDiaChi.getText();
        String sdt = txtSDT.getText();
        
        HoaDon hd = new HoaDon(new NhanVien(serviceNhanvien.getIdByTen(tenNhanVien).getiD()), new KhachHang(serviceKhachHang.getIdByTen(tenKhachHang).getiD()), ma, Date.valueOf(ngayTao),Date.valueOf(ngayThanhToan) ,Date.valueOf(ngayShip) ,Date.valueOf(ngayNhan) , tinhTrang, tenNguoiNhan, diaChi, sdt);
        JOptionPane.showMessageDialog(this, hoaDonService.add(hd));
        showData(hoaDonService.getAll());
    }//GEN-LAST:event_btnThemHoaDonActionPerformed

