    private void btnAddCTSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCTSPActionPerformed
        // TODO add your handling code here:
      
        String tenSp = cbbIDSP.getSelectedItem().toString();
        String teNSX = cbbIDNSX.getSelectedItem().toString();
        String tenMauSac = cbbIDMauSac.getSelectedItem().toString();
        String tenDong = cbbIDDongSp.getSelectedItem().toString();
        String namBH = txtNBH.getText();
        String moTa = txtMoTa.getText();
        String soLuong = txtsoLuong.getText();
        String giaNhap = txtGiaNhap.getText();
        String giaBan = txtGiaBan.getText();
        ChiTietSanPham ctsp = new ChiTietSanPham(new SanPham(ql.getIdByTen(tenSp).getiD()), new NSX(qlNSX.getIdByTen(teNSX).getiD()), new MauSac(qlMau.getIdByTen(tenMauSac).getiD()), new DongSp(qlDong.getIdByTen(tenDong).getiD()), Integer.valueOf(namBH), moTa, Integer.valueOf(soLuong), Double.valueOf(giaNhap), Double.valueOf(giaBan));
        JOptionPane.showMessageDialog(this, qlCtsp.add(ctsp));
        showDataCTSP(qlCtsp.getAllSanPham());
    }//GEN-LAST:event_btnAddCTSPActionPerformed

