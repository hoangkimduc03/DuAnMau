    private void addcbbIDSp(List<SanPham> list){
        dcm1 = (DefaultComboBoxModel) cbbIDSP.getModel();
        for (SanPham x : list) {
            dcm1.addElement(x);
        }
    }
   private void showDataSanPham(List<SanPham> list){
        dtm1.setRowCount(0);
        for (SanPham x : list) {
            dtm1.addRow(x.toRowData());
        }
    }
    private void showDataCTSP(List<ChiTietSanPhamModels> list){
        dtm2.setRowCount(0);
        for (ChiTietSanPhamModels x : list) {
            dtm2.addRow(x.toRowData());
        }
    }
    private void fillDataSanPHam(int index){
        SanPham sp = ql.getAll().get(index);
        txtID.setText(String.valueOf(sp.getiD()));
        txtMa.setText(sp.getMa());
        txtTen.setText(sp.getTen());
    }
    private void fillDataCTSP(int index){
        ChiTietSanPhamModels ctsp = qlCtsp.getAllSanPham().get(index);
       
        txtIDCTSP.setText(String.valueOf(ctsp.getiD()));
         String id = ctsp.getiD();
        cbbIDSP.setSelectedItem(String.valueOf(qlCtsp.getID(id).getSanPham().getiD()));
        cbbIDNSX.setSelectedItem(String.valueOf(qlCtsp.getID(id).getNsx().getiD()));
        cbbIDMauSac.setSelectedItem(String.valueOf(qlCtsp.getID(id).getMauSac().getiD()));
        cbbIDDongSp.setSelectedItem(String.valueOf(qlCtsp.getID(id).getDongSp().getiD()));
        
        txtNBH.setText(String.valueOf(ctsp.getNamBaoHanh()));
        txtMoTa.setText(ctsp.getMoTa());
        txtsoLuong.setText(String.valueOf(ctsp.getSoLuong()));
        txtGiaNhap.setText(String.valueOf(ctsp.getGiaNhap()));
        txtGiaBan.setText(String.valueOf(ctsp.getGiaBan()));
    }
