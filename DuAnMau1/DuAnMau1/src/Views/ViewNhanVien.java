    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String ma = txtMa.getText();
        String ten = txTen.getText();
        String tenDem = txtTenDem.getText();
        String ho = txtho.getText();
        String ngaySinh = txtNgaySinh.getText();
        String gioiTinh ;
        if(rbNam.isSelected()){
            gioiTinh = "Nam";
        }else{
            gioiTinh = "Nữ";
        }
        String diaChi = txtDiaChi.getText();
        String sDT = txtSoDienThoai.getText();
        String matKhau = txtMatKhau.getText();
        String tenChucVu = cbbTenChucVu.getSelectedItem().toString();
        String tenCuaHang = cbbTenCuaHang.getSelectedItem().toString();
        int trangThai ;
        if(rbConLam.isSelected()){
            trangThai =0;
        }else{
            trangThai = 1;
        }
        
        NhanVien nv = new NhanVien(ma, ten, tenDem, ho, gioiTinh,Date.valueOf(ngaySinh), diaChi, sDT, matKhau, new CuaHang(qlCuaHang.getIdByTen(tenCuaHang).getiD()), new ChucVu(qlChucVu.getIdByTen(tenChucVu).getiD()),trangThai);
        JOptionPane.showMessageDialog(this, ql.add(nv));
        showData(ql.getAll());
    }//GEN-LAST:event_btnThemActionPerformed

