    private void showData(List<NHanVienViewModels> list) {
        dtm.setRowCount(0);
        for (NHanVienViewModels x : list) {
            dtm.addRow(x.toRowData());
        }
    }
    private void FillTable(int index){
        NHanVienViewModels nv = ql.getAll().get(index);
        txtId.setText(nv.getId());
        txtMa.setText(nv.getMa());
        String id = nv.getId();
        txTen.setText(ql.getById(id).getTen());
        txtTenDem.setText(ql.getById(id).getTenDem());
        txtho.setText(ql.getById(id).getHo());
        txtNgaySinh.setText(String.valueOf(nv.getNgaySinh()));
        if(nv.getGioiTinh().equalsIgnoreCase("Nam")){
            rbNam.setSelected(true);
        }else{
            rbNu.setSelected(true);
        }
        
        txtDiaChi.setText(nv.getDiaChi());
        txtSoDienThoai.setText(nv.getSoDienThoai());
        if(nv.getTrangThai() == 0){
            rbConLam.setSelected(true);
        }else{
            rbNghiLam.setSelected(true);
        }
        txtMatKhau.setText(ql.getById(id).getMatKhau());
        cbbTenCuaHang.setSelectedItem(ql.getCHCV(id).getCuaHang().getiD());
        cbbTenChucVu.setSelectedItem(ql.getCHCV(id).getChucVu().getiD());
    }
